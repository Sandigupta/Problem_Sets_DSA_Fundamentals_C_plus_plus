#include<iostream>
using namespace std;

int main(){
    int apple=5;
    cout<<apple<<endl;
    cout<<"Size of int:" <<sizeof(apple)<<endl;

    //char data type
    char sing='&';
    cout<<sing<<endl;
    cout<<"size of char:" <<sizeof(sing)<<endl;

    float bag=9.1234;
    cout<<bag<<endl;
    cout<<"size of bag:" <<sizeof(bag)<<endl;
}